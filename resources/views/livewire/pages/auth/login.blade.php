                <span class="px-2 bg-white dark:bg-gray-800 text-gray-500">New to our platform?</span>
            </div>
        </div>

        <div class="mt-6">
            <a href="{{ route('register') }}" 
               class="w-full inline-flex justify-center items-center px-4 py-2 border border-gray-300 dark:border-gray-600 shadow-sm text-sm font-medium rounded-md text-gray-700 dark:text-gray-300 bg-white dark:bg-gray-800 hover:bg-gray-50 dark:hover:bg-gray-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500 transition-colors duration-200">
